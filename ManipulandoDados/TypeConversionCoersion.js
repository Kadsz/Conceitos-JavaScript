/*
  Type conversion (typecasting) vs Type coersion

  --> Type conversion  - eu altero um dado de um tipo para outro.
  --> Type coersion - o javascript faz a troca.

  * Alteração de um tipo de dado para outro tipo

*/

console.log("9" + 5); // --> forçou um Type coersion

console.log(Number("9") + 5); // --> um Type conversion
