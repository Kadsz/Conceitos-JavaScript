// Tipos de dados

/*

  ## Primitivos ----> não é um objeto, os valores são imutaveis!!! não sofre alterações.
  
  String: "" | '' | ``
  console.log("Carlos Eduardo");
  console.log('Carlos Eduardo');
  console.log(`Carlos Eduardo ${18}`);

  Number: 10 | 20.5 (float) | NaN | Infinity
  console.log(10);
  console.log(20.5);
  console.log(10 / "asdsad");
  console.log(Infinity);

  Boolean: True | False
  console.log(false);
  console.log(true);

  Undefined: indefinido
  console.log(undefined);



  ## Estruturais ----> Tem uma estrutura, recebe propriedades e funcionalidades.

  Object: {} -- {propriedade: "valor"}
  console.log({name: "Carlos Eduardo",idade: 18});

  Array: Uma lista | Agrupamento de dado -- ["Carlos", 18]
  console.log(["Carlos", "Eduardo", 18, 2003])


  
  ## Estruturais e Primitivos ----> ao mesmo tempo é Primitivo e Estrutural

  Null: nulo | obejto que não possui nada dentro | diferente de indefinido
  console.log(null);
  
*/

